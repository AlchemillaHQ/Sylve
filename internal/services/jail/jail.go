func (s *Service) ValidateCreate(ctx context.Context, data jailServiceInterfaces.CreateJailRequest) error {
	if data.Name == "" || !utils.IsValidVMName(data.Name) {
		return fmt.Errorf("invalid_vm_name")
	}

	if data.Hostname != "" && !utils.IsValidHostname(data.Hostname) {
		return fmt.Errorf("invalid_hostname")
	}

	if data.CTID == nil || *data.CTID <= 0 || *data.CTID > 9999 {
		return fmt.Errorf("invalid_ct_id")
	}

	var existingCount int64
	if err := s.DB.Model(&jailModels.Jail{}).
		Where("ct_id = ?", *data.CTID).
		Count(&existingCount).Error; err != nil {
		return fmt.Errorf("failed_to_count_jails: %w", err)
	}
	if existingCount > 0 {
		return fmt.Errorf("jail_with_ctid_already_exists")
	}

	if data.VLAN != nil {
		vlan := *data.VLAN
		if vlan < 0 || vlan > 4095 {
			return fmt.Errorf("invalid_vlan")
		}
	}

	if err := s.validateNoStaleJailCreateArtifacts(ctx, *data.CTID); err != nil {
		return err
	}

	if data.Description != "" && (len(data.Description) < 1 || len(data.Description) > 1024) {
		return fmt.Errorf("invalid_description")
	}

	if data.Type != jailModels.JailTypeFreeBSD && data.Type != jailModels.JailTypeLinux {
		return fmt.Errorf("invalid_jail_type")
	}

	pools, err := s.System.GetUsablePools(ctx)
	if err != nil {
		return fmt.Errorf("failed_to_get_usable_pools: %w", err)
	}

	foundPool := ""
	for _, pool := range pools {
		if pool.Name == data.Pool {
			foundPool = pool.Name
			break
		}
	}

	if foundPool == "" {
		return fmt.Errorf("pool_not_found")
	}

	if data.Base != "" && data.BootstrapName != "" {
		return fmt.Errorf("base_and_bootstrap_name_are_mutually_exclusive")
	}

	if data.Base != "" {
		ex, err := s.FindBaseByUUID(data.Base)
		if err != nil {
			return err
		}

		if _, err := os.Stat(ex); os.IsNotExist(err) {
			return fmt.Errorf("base_path_does_not_exist")
		} else if err != nil {
			return fmt.Errorf("failed_to_stat_base_path: %w", err)
		}

		isDir, err := utils.IsDir(ex)
		if err != nil {
			return fmt.Errorf("failed_to_check_base_path_type: %w", err)
		}
		if !isDir {
			return fmt.Errorf("base_is_not_a_directory")
		}
	} else if data.BootstrapName != "" {
		bootstrapDataset := fmt.Sprintf("%s/sylve/bootstraps/%s", data.Pool, data.BootstrapName)
		bootstrapMount := fmt.Sprintf("/%s/sylve/bootstraps/%s", data.Pool, data.BootstrapName)

		var bRecord jailModels.JailBootstrap
		if err := s.DB.Where("pool = ? AND name = ?", data.Pool, data.BootstrapName).First(&bRecord).Error; err != nil {
			return fmt.Errorf("bootstrap_not_found")
		}
		if bRecord.Status != "completed" {
			return fmt.Errorf("bootstrap_not_completed")
		}

		ds, _ := s.GZFS.ZFS.Get(ctx, bootstrapDataset, false)
		if ds == nil {
			return fmt.Errorf("bootstrap_dataset_does_not_exist")
		}

		if _, err := os.Stat(bootstrapMount); os.IsNotExist(err) {
			return fmt.Errorf("bootstrap_mount_does_not_exist")
		}
	} else {
		return fmt.Errorf("download_uuid_or_bootstrap_name_required")
	}

	existingDataset, err := s.GZFS.ZFS.Get(ctx, fmt.Sprintf("%s/sylve/jails/%d", foundPool, *data.CTID), false)
	if err != nil {
		if !strings.Contains(err.Error(), "dataset does not exist") {
			return fmt.Errorf("failed_to_get_existing_datasets: %w", err)
		}
	}

	if existingDataset != nil {
		return fmt.Errorf("jail_base_fs_with_ctid_already_exists")
	}

	swAvailable := true
	mac := uint(0)
	dhcp := false
	slaac := false

	if data.SwitchName == "" {
		return fmt.Errorf("switch_name_required")
	} else if strings.ToLower(data.SwitchName) == "inherit" || strings.ToLower(data.SwitchName) == "none" {
		swAvailable = false
	}

	if swAvailable {
		found := false

		var stdSwitch networkModels.StandardSwitch
		if err := s.DB.First(&stdSwitch, "name = ?", data.SwitchName).Error; err == nil {
			found = true
		}

		var manualSwitch networkModels.ManualSwitch
		if err := s.DB.First(&manualSwitch, "name = ?", data.SwitchName).Error; err == nil {
			found = true
		}

		if !found {
			return fmt.Errorf("standard_switch_not_found")
		}
	}

	if data.MAC != nil {
		mac = uint(*data.MAC)
		if mac != 0 && swAvailable {
			used, _, err := s.NetworkService.IsObjectUsed(mac)
			if err != nil {
				return fmt.Errorf("failed_to_check_mac_usage: %w", err)
			}

			if used {
				return fmt.Errorf("mac_already_used")
			}
		}
	}

	if data.DHCP != nil {
		dhcp = *data.DHCP
	}

	if data.SLAAC != nil {
		slaac = *data.SLAAC
	}

	if swAvailable {
		if data.MACRaw != "" && !utils.IsValidMAC(data.MACRaw) {
			return fmt.Errorf("invalid_mac")
		}

		if data.IPv4Raw != "" && !utils.IsValidIPv4CIDR(data.IPv4Raw) {
			return fmt.Errorf("invalid_ipv4_cidr")
		}

		if data.IPv4GwRaw != "" && !utils.IsValidIPv4(data.IPv4GwRaw) {
			return fmt.Errorf("invalid_ipv4_gateway")
		}

		if data.IPv6Raw != "" && !utils.IsValidIPv6CIDR(data.IPv6Raw) {
			return fmt.Errorf("invalid_ipv6_cidr")
		}

		if data.IPv6GwRaw != "" && !utils.IsValidIPv6(data.IPv6GwRaw) {
			return fmt.Errorf("invalid_ipv6_gateway")
		}

		if !dhcp {
			if data.IPv4 != nil {
				ipv4Id := uint(*data.IPv4)
				if ipv4Id != 0 && data.IPv4Gw != nil {
					ipv4GwId := uint(*data.IPv4Gw)
					if ipv4GwId == 0 && data.IPv4GwRaw == "" {
						return fmt.Errorf("invalid_ipv4_gateway")
					}

					isUsed, _, err := s.NetworkService.IsObjectUsed(ipv4Id)
					if err != nil {
						return fmt.Errorf("failed_to_check_ipv4_usage: %w", err)
					}

					if isUsed {
						return fmt.Errorf("ipv4_already_used")
					}
				}
			}
		}

		if !slaac {
			if data.IPv6 != nil {
				ipv6Id := uint(*data.IPv6)

				if ipv6Id != 0 && data.IPv6Gw != nil {
					ipv6GwId := uint(*data.IPv6Gw)
					if ipv6GwId == 0 && data.IPv6GwRaw == "" {
						return fmt.Errorf("invalid_ipv6_gateway")
					}

					isUsed, _, err := s.NetworkService.IsObjectUsed(ipv6Id)
					if err != nil {
						return fmt.Errorf("failed_to_check_ipv6_usage: %w", err)
					}

					if isUsed {
						return fmt.Errorf("ipv6_already_used")
					}
				}
			}
		}
	}

	if data.Type == jailModels.JailTypeLinux {
		if dhcp || slaac {
			return fmt.Errorf("linux_jails_cannot_use_dhcp_or_slaac")
		}
	}

	if !utils.IsValidJailAllowedOpts(data.AllowedOptions) {
		return fmt.Errorf("invalid_jail_allowed_options")
	}

	if config.IsDevFSDisabled() && slices.Contains(data.AllowedOptions, "allow.mount.devfs") {
		return fmt.Errorf("devfs_management_disabled")
	}

	if data.StartOrder < 0 {
		return fmt.Errorf("start_order_must_be_greater_than_or_equal_to_0")
	}

	if s.guestIdentityChecker != nil {
		if err := s.guestIdentityChecker.RequireGuestIDAvailable(ctx, *data.CTID); err != nil {
			return err
		}
	}

	return nil
}

func isZFSDatasetMissingError(err error) bool {
	if err == nil {
		return false
	}

	msg := strings.ToLower(err.Error())
	return strings.Contains(msg, "dataset does not exist") ||
		strings.Contains(msg, "no such dataset") ||
		strings.Contains(msg, "does not exist")
}

func isZFSDatasetBusyError(err error) bool {
	if err == nil {
		return false
	}

	msg := strings.ToLower(err.Error())
	return strings.Contains(msg, "dataset is busy") ||
		strings.Contains(msg, "resource busy")
}

func isZFSDatasetDependentCloneError(err error) bool {
	if err == nil {
		return false
	}

	msg := strings.ToLower(err.Error())
	return strings.Contains(msg, "dependent clones") ||
		strings.Contains(msg, "filesystem has dependent clones")
}

func isRetriableZFSDestroyError(err error) bool {
	return isZFSDatasetBusyError(err) || isZFSDatasetDependentCloneError(err)
}

const jailDeleteStopTimeout = 10 * time.Second

type jailDeletePlan struct {
	jailID       uint
	ctID         uint
	rootDatasets []string
	macIDs       []uint
}

type jailDeleteRuntime struct {
	isRunning    func(uint) (bool, error)
	stop         func(uint) error
	removeConfig func(string) error
	removeDevfs  func(uint) error
}

func (s *Service) hostJailDeleteRuntime() jailDeleteRuntime {
	return jailDeleteRuntime{
		isRunning: s.IsJailRunning,
		stop: func(ctID uint) error {
			return s.JailAction(int(ctID), "stop")
		},
		removeConfig: os.RemoveAll,
		removeDevfs: func(ctID uint) error {
			if config.IsDevFSDisabled() {
				return nil
			}
			if _, err := os.Stat("/etc/devfs.rules"); err != nil {
				if errors.Is(err, os.ErrNotExist) {
					return nil
				}
				return fmt.Errorf("failed_to_stat_devfs_rules: %w", err)
			}
			return s.RemoveDevfsRulesForCTID(ctID)
		},
	}
}

func ensureJailStoppedForDelete(ctx context.Context, ctID uint, runtime jailDeleteRuntime) error {
	if ctx == nil {
		ctx = context.Background()
	}
	if runtime.isRunning == nil || runtime.stop == nil {
		return fmt.Errorf("jail_delete_runtime_not_initialized")
	}

	running, err := runtime.isRunning(ctID)
	if err != nil {
		return fmt.Errorf("failed_to_check_jail_runtime_before_delete: %w", err)
	}
	if !running {
		return nil
	}

	if err := runtime.stop(ctID); err != nil {
		return fmt.Errorf("failed_to_stop_jail_before_delete: %w", err)
	}

	ticker := time.NewTicker(250 * time.Millisecond)
	defer ticker.Stop()
	timeout := time.NewTimer(jailDeleteStopTimeout)
	defer timeout.Stop()

	for {
		running, err = runtime.isRunning(ctID)
		if err != nil {
			return fmt.Errorf("failed_to_verify_jail_stopped_before_delete: %w", err)
		}
		if !running {
			return nil
		}

		select {
		case <-ctx.Done():
			return fmt.Errorf("jail_stop_verification_canceled: %w", ctx.Err())
		case <-timeout.C:
			return fmt.Errorf("jail_failed_to_stop_before_delete")
		case <-ticker.C:
		}
	}
}

func (s *Service) destroyStorageDatasetForDelete(ctx context.Context, datasetName string) error {
	if s.GZFS == nil || s.GZFS.ZFS == nil {
		return fmt.Errorf("zfs_client_not_initialized")
	}

	datasetName = strings.TrimSpace(datasetName)
	if datasetName == "" {
		return nil
	}

	const maxAttempts = 8
	const retryDelay = 750 * time.Millisecond

	var lastErr error

	for attempt := 1; attempt <= maxAttempts; attempt++ {
		dataset, err := s.GZFS.ZFS.Get(ctx, datasetName, true)
		if err != nil {
			if isZFSDatasetMissingError(err) {
				return nil
			}
			return fmt.Errorf("failed_to_get_dataset_for_storage: %w", err)
		}
		if dataset == nil {
			return nil
		}

		err = dataset.Destroy(ctx, true, false)
		if err == nil || isZFSDatasetMissingError(err) {
			return nil
		}

		lastErr = err
		if attempt == maxAttempts || !isRetriableZFSDestroyError(lastErr) {
			break
		}

		select {
		case <-ctx.Done():
			return fmt.Errorf("failed_to_destroy_storage_dataset_canceled: %w", ctx.Err())
		case <-time.After(retryDelay):
		}
	}

	return fmt.Errorf("failed_to_destroy_storage_dataset_after_retries: %w", lastErr)
}

func appendJailCreateCleanupWarning(warnings *[]string, ctid uint, code string, err error) {
	message := code
	if err != nil {
		message = fmt.Sprintf("%s: %v", code, err)
		logger.L.Warn().Uint("ctid", ctid).Err(err).Msg(code)
	} else {
		logger.L.Warn().Uint("ctid", ctid).Msg(code)
	}

	*warnings = append(*warnings, message)
}

func uniqueUintValues(values []uint) []uint {
	if len(values) == 0 {
		return []uint{}
	}

	seen := make(map[uint]struct{}, len(values))
	out := make([]uint, 0, len(values))
	for _, value := range values {
		if value == 0 {
			continue
		}

		if _, exists := seen[value]; exists {
			continue
		}

		seen[value] = struct{}{}
		out = append(out, value)
	}

	return out
}

func (s *Service) hasStaleJailRootDatasetForCreate(ctx context.Context, ctid uint) (bool, error) {
	if s.System == nil {
		return false, fmt.Errorf("system_service_not_initialized")
	}

	if s.GZFS == nil || s.GZFS.ZFS == nil {
		return false, fmt.Errorf("zfs_client_not_initialized")
	}

	pools, err := s.System.GetUsablePools(ctx)
	if err != nil {
		return false, fmt.Errorf("failed_to_list_usable_pools_for_jail_create_precheck: %w", err)
	}

	for _, pool := range pools {
		if pool == nil {
			continue
		}

		poolName := strings.TrimSpace(pool.Name)
		if poolName == "" {
			continue
		}

		datasetName := fmt.Sprintf("%s/sylve/jails/%d", poolName, ctid)
		ds, getErr := s.GZFS.ZFS.Get(ctx, datasetName, false)
		if getErr != nil {
			if isZFSDatasetMissingError(getErr) {
				continue
			}

			return false, fmt.Errorf("failed_to_check_jail_root_dataset_for_create_precheck: %w", getErr)
		}

		if ds != nil {
			return true, nil
		}
	}

	return false, nil
}

func (s *Service) validateNoStaleJailCreateArtifacts(ctx context.Context, ctid uint) error {
	rootDatasetExists, err := s.hasStaleJailRootDatasetForCreate(ctx, ctid)
	if err != nil {
		return err
	}

	if rootDatasetExists {
		return fmt.Errorf(
			"jail_create_stale_artifacts_detected: ctid=%d root_dataset_exists=%t",
			ctid,
			rootDatasetExists,
		)
	}

	return nil
}

func (s *Service) forceRemoveJailRuntimeArtifacts(ctid uint, warnings *[]string) {
	jailsPath, err := config.GetJailsPath()
	if err != nil {
		appendJailCreateCleanupWarning(warnings, ctid, "failed_to_get_jails_path_for_create_rollback", err)
		return
	}

	jailDir := filepath.Join(jailsPath, fmt.Sprintf("%d", ctid))
	if _, statErr := os.Stat(jailDir); statErr == nil {
		if removeErr := os.RemoveAll(jailDir); removeErr != nil {
			appendJailCreateCleanupWarning(warnings, ctid, "failed_to_remove_jail_runtime_dir_for_create_rollback", removeErr)
		}
	}
}

func (s *Service) forceRemoveJailZFSDatasets(ctx context.Context, ctid uint, fallbackPool string, warnings *[]string) {
	if s.GZFS == nil || s.GZFS.ZFS == nil {
		appendJailCreateCleanupWarning(warnings, ctid, "zfs_client_not_initialized_during_jail_create_rollback", fmt.Errorf("zfs_client_not_initialized"))
		return
	}

	poolNames := make(map[string]struct{})
	if fallbackPool = strings.TrimSpace(fallbackPool); fallbackPool != "" {
		poolNames[fallbackPool] = struct{}{}
	}

	if s.System != nil {
		pools, err := s.System.GetUsablePools(ctx)
		if err != nil {
			appendJailCreateCleanupWarning(warnings, ctid, "failed_to_list_usable_pools_for_jail_create_rollback", err)
		} else {
			for _, pool := range pools {
				if pool == nil {
					continue
				}

				poolName := strings.TrimSpace(pool.Name)
				if poolName == "" {
					continue
				}

				poolNames[poolName] = struct{}{}
			}
		}
	}

	var jailIDs []uint
	if err := s.DB.Model(&jailModels.Jail{}).
		Where("ct_id = ?", ctid).
		Pluck("id", &jailIDs).Error; err != nil {
		appendJailCreateCleanupWarning(warnings, ctid, "failed_to_lookup_jail_ids_for_zfs_rollback", err)
	}

	if len(jailIDs) > 0 {
		var storagePools []string
		if err := s.DB.Model(&jailModels.Storage{}).
			Where("jid IN ?", jailIDs).
			Where("pool IS NOT NULL AND pool <> ''").
			Pluck("pool", &storagePools).Error; err != nil {
			appendJailCreateCleanupWarning(warnings, ctid, "failed_to_lookup_jail_storage_pools_for_zfs_rollback", err)
		} else {
			for _, pool := range storagePools {
				poolName := strings.TrimSpace(pool)
				if poolName == "" {
					continue
				}

				poolNames[poolName] = struct{}{}
			}
		}
	}

	for poolName := range poolNames {
		datasetName := fmt.Sprintf("%s/sylve/jails/%d", poolName, ctid)

		ds, getErr := s.GZFS.ZFS.Get(ctx, datasetName, false)
		if getErr != nil {
			if isZFSDatasetMissingError(getErr) {
				continue
			}
			appendJailCreateCleanupWarning(
				warnings,
				ctid,
				fmt.Sprintf("failed_to_get_jail_dataset_%s_for_create_rollback", datasetName),
				getErr,
			)
			continue
		}

		if ds == nil {
			continue
		}

		if destroyErr := ds.Destroy(ctx, true, false); destroyErr != nil && !isZFSDatasetMissingError(destroyErr) {
			appendJailCreateCleanupWarning(
				warnings,
				ctid,
				fmt.Sprintf("failed_to_destroy_jail_dataset_%s_for_create_rollback", datasetName),
				destroyErr,
			)
		}
	}
}


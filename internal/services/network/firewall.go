func buildPFDisabledConfig() string {
	var b strings.Builder
	b.WriteString(pfManagedHeader)
	b.WriteString("\n\n")
	b.WriteString("# firewall disabled in Sylve; keep default network access open\n")
	b.WriteString("pass all\n")
	return b.String()
}

func parseRCConfAssignment(line string) (string, string, bool) {
	trimmed := strings.TrimSpace(line)
	if trimmed == "" || strings.HasPrefix(trimmed, "#") || strings.HasPrefix(trimmed, ";") {
		return "", "", false
	}

	index := strings.Index(trimmed, "=")
	if index < 0 {
		return "", "", false
	}

	key := strings.TrimSpace(trimmed[:index])
	if key == "" {
		return "", "", false
	}

	rawValue := strings.TrimSpace(trimmed[index+1:])
	rawValue = strings.Trim(rawValue, `"'`)
	rawValue = strings.TrimSpace(rawValue)
	return key, rawValue, true
}

func (s *Service) ensurePFAutostartDisabledInRCConf() error {
	data, err := os.ReadFile(firewallRCConfPath)
	if err != nil && !strings.Contains(strings.ToLower(err.Error()), "no such file") {
		return err
	}

	lines := []string{}
	if len(data) > 0 {
		lines = strings.Split(strings.TrimSuffix(string(data), "\n"), "\n")
	}

	updated := make([]string, 0, len(lines)+1)
	seenPFEnable := false
	changed := false

	for _, line := range lines {
		key, value, ok := parseRCConfAssignment(line)
		if !ok || key != "pf_enable" {
			updated = append(updated, line)
			continue
		}

		if seenPFEnable {
			changed = true
			continue
		}

		seenPFEnable = true
		if strings.EqualFold(value, "no") {
			updated = append(updated, `pf_enable="NO"`)
			if line != `pf_enable="NO"` {
				changed = true
			}
			continue
		}

		updated = append(updated, `pf_enable="NO"`)
		changed = true
	}

	if !seenPFEnable {
		updated = append(updated, `pf_enable="NO"`)
		changed = true
	}

	if !changed {
		return nil
	}

	perm := os.FileMode(0644)
	if info, statErr := os.Stat(firewallRCConfPath); statErr == nil {
		perm = info.Mode().Perm()
	}

	content := ""
	if len(updated) > 0 {
		content = strings.Join(updated, "\n") + "\n"
	}

	return atomicWriteFile(firewallRCConfPath, []byte(content), perm)
}

func (s *Service) isSylveManagedPFConfig() (bool, error) {
	data, err := os.ReadFile(pfMainConfPath)
	if err != nil {
		if strings.Contains(strings.ToLower(err.Error()), "no such file") {
			return false, nil
		}
		return false, err
	}

	content := string(data)
	return strings.Contains(content, pfManagedHeader), nil
}

func (s *Service) IsFirewallServiceEnabled() bool {
	var basicSettings models.BasicSettings
	if err := s.DB.First(&basicSettings).Error; err != nil {
		return false
	}

	return slices.Contains(basicSettings.Services, models.Firewall)
}

func (s *Service) ApplyFirewallIfEnabled() error {
	if !s.IsFirewallServiceEnabled() {
		return nil
	}

	return s.ApplyFirewallConfig()
}

func (s *Service) SyncFirewallRuntimeState() error {
	if err := s.ensurePFAutostartDisabledInRCConf(); err != nil {
		return err
	}

	if s.IsFirewallServiceEnabled() {
		return s.ApplyFirewallConfig()
	}

	if !s.isPFEnabled() {
		return nil
	}

	managed, err := s.isSylveManagedPFConfig()
	if err != nil {
		return err
	}
	if !managed {
		return nil
	}

	return s.DisableFirewall()
}

func (s *Service) DisableFirewall() error {
	if err := s.ensurePFAutostartDisabledInRCConf(); err != nil {
		return err
	}

	if err := atomicWriteFile(pfMainConfPath, []byte(buildPFDisabledConfig()), 0644); err != nil {
		return err
	}

	err := system.ServiceAction("pf", "onestop")
	if err != nil {
		if strings.Contains(strings.ToLower(err.Error()), "not running") {
			return nil
		}
		return err
	}

	return nil
}


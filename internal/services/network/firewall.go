func (s *Service) GetFirewallTrafficRuleCounters() ([]networkServiceInterfaces.FirewallTrafficRuleCounter, error) {
	rules, err := s.GetFirewallTrafficRules()
	if err != nil {
		return nil, err
	}

	if len(rules) == 0 {
		return []networkServiceInterfaces.FirewallTrafficRuleCounter{}, nil
	}

	updatedAt := time.Now().UTC()
	totalsByRuleID := make(map[uint]trafficRuleCounterTotals)

	if s.isPFEnabled() {
		if totals, _, pfErr := s.collectTrafficCountersFromPF(); pfErr == nil {
			totalsByRuleID = totals
		}
	}

	counters := make([]networkServiceInterfaces.FirewallTrafficRuleCounter, 0, len(rules))
	for _, rule := range rules {
		totals := totalsByRuleID[rule.ID]
		counters = append(counters, networkServiceInterfaces.FirewallTrafficRuleCounter{
			ID:        rule.ID,
			Packets:   totals.Packets,
			Bytes:     totals.Bytes,
			UpdatedAt: updatedAt,
		})
	}

	return counters, nil
}


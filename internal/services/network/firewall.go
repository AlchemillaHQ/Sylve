func normalizeInterfaceList(values []string) []string {
	if len(values) == 0 {
		return []string{}
	}

	seen := make(map[string]struct{}, len(values))
	out := make([]string, 0, len(values))
	for _, value := range values {
		iface := strings.TrimSpace(value)
		if iface == "" {
			continue
		}
		if _, ok := seen[iface]; ok {
			continue
		}
		seen[iface] = struct{}{}
		out = append(out, iface)
	}
	return out
}

func hasSelector(raw string, objID *uint) bool {
	return strings.TrimSpace(raw) != "" || (objID != nil && *objID > 0)
}

func validateFamilyAgainstRawAddress(value string, family string, allowCIDR bool, field string, allowAnyLiteral bool) error {
	v := strings.TrimSpace(value)
	if v == "" {
		return nil
	}

	if allowAnyLiteral && strings.EqualFold(v, "any") {
		return nil
	}

	isV4 := utils.IsValidIPv4(v)
	isV6 := utils.IsValidIPv6(v)
	isV4CIDR := utils.IsValidIPv4CIDR(v)
	isV6CIDR := utils.IsValidIPv6CIDR(v)

	if !(isV4 || isV6 || isV4CIDR || isV6CIDR) {
		return fmt.Errorf("invalid_%s_address: %s", field, v)
	}

	if !allowCIDR && (isV4CIDR || isV6CIDR) {
		return fmt.Errorf("%s_must_be_host_address", field)
	}

	switch family {
	case "inet":
		if isV6 || isV6CIDR {
			return fmt.Errorf("incompatible_family_for_%s: expected_ipv4", field)
		}
	case "inet6":
		if isV4 || isV4CIDR {
			return fmt.Errorf("incompatible_family_for_%s: expected_ipv6", field)
		}
	}

	return nil
}

func hasPortSelector(raw string, objID *uint) bool {
	return strings.TrimSpace(raw) != "" || (objID != nil && *objID > 0)
}

func validateRawPortSelector(raw string, field string) error {
	trimmed := strings.TrimSpace(raw)
	if trimmed == "" {
		return nil
	}

	if strings.HasPrefix(trimmed, "{") && strings.HasSuffix(trimmed, "}") {
		trimmed = strings.TrimSpace(strings.TrimSuffix(strings.TrimPrefix(trimmed, "{"), "}"))
	}

	tokens := strings.Split(trimmed, ",")
	for _, token := range tokens {
		part := strings.TrimSpace(token)
		if part == "" {
			return fmt.Errorf("invalid_%s_port_selector: empty_token", field)
		}
		if !parsePortValue(part) {
			return fmt.Errorf("invalid_%s_port_selector: %s", field, part)
		}
	}

	return nil
}

